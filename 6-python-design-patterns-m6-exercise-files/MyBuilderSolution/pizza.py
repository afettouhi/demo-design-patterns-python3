class Pizza(object):

    def display(self):
        print('Custom Pizza:')
        print('\t{:>10}: {}'.format('Crust', self.crust))
        print('\t{:>10}: {}'.format('Base', self.base))
        print('\t{:>10}: {}'.format('Meat Toppings', self.meat_toppings))
        print('\t{:>10}: {}'.format('Vegetable Toppings', self.vegetable_toppings))
        print('\t{:>10}: {}'.format('Cheese Toppings', self.cheese_toppings))





